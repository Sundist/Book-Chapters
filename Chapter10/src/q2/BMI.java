public class BMI {

    private String name = "Kaan";
    private int age = 30;
    private double weight = 80;
    private double feet = 44;
    private double inches = 29;

    BMI() {
    }

    BMI(String name, int age, double weight, double feet, double inches) {
        this.name = name;
        this.age = age;
        this.weight = weight;
        this.feet = feet;
        this.inches = inches;
    }
}
